// Utility functions for logo management
export const LogoUtils = {
  /**
   * Get league logo URL by league key
   * @param {string} leagueKey - The league identifier
   * @returns {string} Logo URL or fallback
   */
  getLeagueLogo(leagueKey) {
    return LEAGUE_LOGOS[leagueKey] || LEAGUE_LOGOS['premier-league']; // Fallback to Premier League
  },

  /**
   * Get team logo URL by team name
   * @param {string} teamName - The team name
   * @returns {string} Logo URL or fallback
   */
  getTeamLogo(teamName) {
    // Try exact match first
    if (TEAM_LOGOS[teamName]) {
      return TEAM_LOGOS[teamName];
    }
    
    // Try case-insensitive search
    const teamKey = Object.keys(TEAM_LOGOS).find(
      key => key.toLowerCase() === teamName.toLowerCase()
    );
    
    if (teamKey) {
      return TEAM_LOGOS[teamKey];
    }
    
    // Try partial match for team names with variations
    const partialMatch = Object.keys(TEAM_LOGOS).find(
      key => key.toLowerCase().includes(teamName.toLowerCase()) || 
             teamName.toLowerCase().includes(key.toLowerCase())
    );
    
    return partialMatch ? TEAM_LOGOS[partialMatch] : this.getDefaultTeamLogo();
  },

  /**
   * Get default team logo when no match is found
   * @returns {string} Default logo URL
   */
  getDefaultTeamLogo() {
    return 'https://via.placeholder.com/64x64/0e639c/ffffff?text=FC';
  },

  /**
   * Get default league logo when no match is found
   * @returns {string} Default logo URL
   */
  getDefaultLeagueLogo() {
    return 'https://via.placeholder.com/64x64/0e639c/ffffff?text=⚽';
  },

  /**
   * Check if a team logo exists
   * @param {string} teamName - The team name
   * @returns {boolean} Whether logo exists
   */
  hasTeamLogo(teamName) {
    return Boolean(this.getTeamLogo(teamName) !== this.getDefaultTeamLogo());
  },

  /**
   * Check if a league logo exists
   * @param {string} leagueKey - The league identifier
   * @returns {boolean} Whether logo exists
   */
  hasLeagueLogo(leagueKey) {
    return Boolean(LEAGUE_LOGOS[leagueKey]);
  },

  /**
   * Get all available leagues
   * @returns {Array} Array of league keys
   */
  getAllLeagues() {
    return Object.keys(LEAGUE_LOGOS);
  },

  /**
   * Get all available teams
   * @returns {Array} Array of team names
   */
  getAllTeams() {
    return Object.keys(TEAM_LOGOS);
  },

  /**
   * Create an img element with logo
   * @param {string} logoUrl - The logo URL
   * @param {string} alt - Alt text
   * @param {string} className - CSS class name
   * @returns {HTMLElement} Image element
   */
  createLogoElement(logoUrl, alt = 'Logo', className = 'logo') {
    const img = document.createElement('img');
    img.src = logoUrl;
    img.alt = alt;
    img.className = className;
    img.onerror = () => {
      img.src = this.getDefaultTeamLogo();
    };
    return img;
  },

  /**
   * Preload logos for better performance
   * @param {Array} logoUrls - Array of logo URLs to preload
   */
  preloadLogos(logoUrls) {
    logoUrls.forEach(url => {
      const img = new Image();
      img.src = url;
    });
  }
};

// Export default for easy importing
export default {
  LEAGUE_LOGOS,
  TEAM_LOGOS,
  LogoUtils
};
